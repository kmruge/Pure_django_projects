  function applyStyle(style) {
    const textarea = document.getElementById('kb_desc');
    const selectedText = textarea.value.substring(textarea.selectionStart, textarea.selectionEnd);
    
    let updatedText;
    switch (style) {
      case 'bold':
        updatedText = `<b>${selectedText}</b>`;
        break;
      case 'italic':
        updatedText = `<i>${selectedText}</i>`;
        break;
      case 'underline':
        updatedText = `<u>${selectedText}</u>`;
        break;
      default:
        updatedText = selectedText;
        break;
    }
    
    const textBefore = textarea.value.substring(0, textarea.selectionStart);
    const textAfter = textarea.value.substring(textarea.selectionEnd);
    textarea.value = textBefore + updatedText + textAfter;
  }
  
